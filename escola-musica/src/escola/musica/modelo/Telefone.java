public class Telefone {
	
		private String numero;

		/**
		 * @param numero
		 */
		public Telefone(String numero) {
			super();
			this.numero = numero;
		}

		/**
		 * 
		 */
		public Telefone() {
			super();
		}

		/**
		 * @return the numero
		 */
		public String getNumero() {
			return numero;
		}

		/**
		 * @param numero the numero to set
		 */
		public void setNumero(String numero) {
			this.numero = numero;
		}
		
}
